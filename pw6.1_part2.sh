#!/bin/bash

# Определение цветовых кодов
RED='\033[0;31m'    # Красный
GREEN='\033[0;32m'  # Зеленый
YELLOW='\033[0;33m' # Желтый
NC='\033[0m'        # Сброс цвета

# Проверка на выполнение от имени root
if [ "$EUID" -ne 0 ]; then
    echo -e "${RED}Пожалуйста, запустите этот скрипт с правами суперпользователя (root).${NC}" 
    exit 1
fi

# 5. Создаем каталог SSH931 и подкаталог Best
echo -e "${YELLOW}Создаем каталоги SSH931 и Best...${NC}"
sudo mkdir -p /home/SSH931/Best
echo -e "${GREEN}Каталоги созданы.${NC}"

# 6. Открываем полный доступ к каталогу Best
echo -e "${YELLOW}Открываем полный доступ к каталогу Best...${NC}"
sudo chmod 777 /home/SSH931/Best

# 7. Создаем текстовый файл в каталоге Best
echo -e "${YELLOW}Создаем текстовый файл korobeynikov.txt в каталоге Best...${NC}"
echo 'Samba позволяет компьютерам на базе Linux полноценно обмениваться файлами и другими ресурсами с машинами под управлением Windows.' | sudo tee /home/SSH931/Best/korobeynikov.txt > /dev/null

# 8. Разрываем соединение SSH (инструкция для пользователя)
echo -e "${GREEN}Чтобы разорвать соединение SSH, введите: exit${NC}"